    void listar(){
        String sql="select * from usuario";
        try {
            cn=con.getConnection();
            st=cn.createStatement();
            rs=st.executeQuery(sql);
            Object[]usuario=new Object[6];
            modelo=(DefaultTableModel)tablaDatos.getModel();
            while (rs.next()) {                
                usuario[0]=rs.getInt("idusuario");
                usuario[1]=rs.getString("nom");
                usuario[2]=rs.getString("apellido");
                usuario[3]=rs.getString("dni");
                usuario[5]=rs.getString("usuario");
                usuario[3]=rs.getString("contrasenia");
                modelo.addRow(usuario);
            }
            tablaDatos.setModel(modelo);
        } catch (Exception e) {
        }
    }

